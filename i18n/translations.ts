/** Yer tutucu değiştir: {{key}} → value */
export function interpolate(
  text: string,
  vars: Record<string, string | number>
): string {
  return Object.entries(vars).reduce(
    (acc, [key, value]) => acc.replace(new RegExp(`\\{\\{${key}\\}\\}`, 'g'), String(value)),
    text
  );
}
